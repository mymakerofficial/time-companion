export const colors = [
  'red',
  'orange',
  'amber',
  'yellow',
  'lime',
  'green',
  'emerald',
  'teal',
  'cyan',
  'sky',
  'blue',
  'indigo',
  'violet',
  'purple',
  'fuchsia',
  'pink',
  'rose',
]

export function randomTailwindColor() {
  const randomIndex = Math.floor(Math.random() * colors.length)
  return colors[randomIndex]
}
